								<h2 style="text-align: justify;"><span class="ez-toc-section" id="Tai_sao_can_kieng_ky_giuong_ngu_bi_xa_nha_de"></span><b>Tại sao cần kiêng kỵ giường ngủ bị xà nhà đè?</b><span class="ez-toc-section-end"></span></h2>

								<p style="text-align: justify;"><span style="font-weight: 400;">Bạn có biết nguyên nhân tại sao </span><b>giường ngủ bị xà nhà đè</b><span style="font-weight: 400;"> lại là điều tối kỵ? Xà nhà (một số nơi gọi là xà ngang, dầm nhà hay dầm xà). Xà nhà có tác dụng đảm bảo kết cấu vững chắc cho một ngôi nhà, nâng đỡ mái nhà, có thể làm bằng gỗ với những thiết kế nhà cổ hoặc bê tông cốt thép với nhà hiện đại.&nbsp;</span></p>

								<p style="text-align: justify;"><span style="font-weight: 400;">Tuy nhiên, theo quan niệm của các chuyên gia phong thủy, xà nhà được xem như một vật cản và không tốt cho sự lưu chuyển khí trong nhà. Xà nhà thường nặng nề, gây sức đè nén xuống dưới, và trần nhà càng thấp thì càng tạo sự ngột ngạt, bớt đi nguyên khí trong nhà. Vậy nên, giường ngủ dưới xà nhà chính là huyền trâm sát, gây tổn nhân khẩu và có ảnh hưởng xấu đến gia chủ.&nbsp;</span></p>
								<p style="text-align: justify;"><span style="font-weight: 400;">Có thể thấy, khi nằm ngủ trên giường, mắt sẽ hướng nhìn lên trần nhà, xà nhà to lớn sẽ tạo tâm lý nặng nề. Với những người có tâm lý yếu, nhạy cảm sẽ cảm giác xà đè lên người hoặc mơ hồ thành ám ảnh rằng xà nhà bị rơi xuống mà luôn trong trạng thái bất an, khó ngủ, ảnh hưởng đến sức khỏe lâu dài.</span></p>
								<p style="text-align: justify;"><span style="font-weight: 400;">Vị trí xà ở đầu giường có thể gây nhức đầu, buồn nôn, hệ tiêu hóa kém. Vị trí xà ở cuối giường sẽ gây cản trở sự thay đổi, gây trì trệ, khó khăn trong thăng tiến của các thành viên cư ngụ. Một số người làm kinh doanh, giao thương có thể sẽ gặp rủi ro, việc giao du không mấy thuận lợi, chuyện đi lại giảm đi khi kê giường ngủ dưới xà nhà.</span></p>
